class plusplus
{
	
	public static void main(String[] args)
	{
		int [] b = {4,5,6,7,8};
		int index = 0;

		System.out.println("b[index] = " +b[index]);
		System.out.println("b[index++] = " +b[index++]);
		System.out.println("b[index] = " +b[index]);
		System.out.println("b[++index] = " +b[++index]);
		System.out.println("b[index] = " +b[index]);



	}
}